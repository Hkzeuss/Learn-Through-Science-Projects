# Lambda function tính bình phương của số
square = lambda x: x ** 2
print(square(5))  # Kết quả: 25

# Lambda function tính tổng của hai số
add = lambda a, b: a + b
print(add(3, 7))  # Kết quả: 10

# Lambda function kiểm tra số chẵn
is_even = lambda x: x % 2 == 0
print(is_even(4))  # Kết quả: True
print(is_even(7))  # Kết quả: False